package com.aantik.demo.model;


public class actualizarDatosEs {

	private String telefono;
	private String municipio;
	private String localidad;	
	private String barrio;
	private String direccion;
	private String limitaciones;
	private String localidadrest;
	private String[] notif;
	
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getMunicipio() {
		return municipio;
	}
	public void setMunicipio(String municipio) {
		this.municipio = municipio;
	}
	public String getLocalidad() {
		return localidad;
	}
	public void setLocalidad(String localidad) {
		this.localidad = localidad;
	}
	public String getBarrio() {
		return barrio;
	}
	public void setBarrio(String barrio) {
		this.barrio = barrio;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getLimitaciones() {
		return limitaciones;
	}
	public void setLimitaciones(String limitaciones) {
		this.limitaciones = limitaciones;
	}
	public String getLocalidadrest() {
		return localidadrest;
	}
	public void setLocalidadrest(String localidadrest) {
		this.localidadrest = localidadrest;
	}
	public String[] getNotif() {
		return notif;
	}
	public void setNotif(String[] notif) {
		this.notif = notif;
	}

	
	
}
